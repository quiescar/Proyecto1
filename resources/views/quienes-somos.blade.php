    El Campeonato Mundial de Fórmula 1 de la FIA, más conocida como Fórmula Uno, Fórmula 1 o F1, es la principal competición
    de automovilismo internacional y campeonato de deportes de motor más popular y prestigioso del mundo. La entidad que la
    dirige es la Federación Internacional del Automóvil (FIA). En septiembre del 2016, la empresa estadounidense Liberty
    Media, a través de la adquisición del Formula One Group, es responsable actualmente de gestionar y operar el
    campeonato.<br>

    A cada carrera se le denomina Gran Premio y el torneo que las agrupa se denomina Campeonato Mundial de Fórmula 1. La
    mayoría de los circuitos de carreras donde se celebran los Grandes Premios son autódromos, aunque también se utilizan
    circuitos callejeros y anteriormente se utilizaron circuitos ruteros. A su vez, los automóviles utilizados son
    monoplazas con la última tecnología disponible, siempre limitadas por un reglamento técnico; algunas mejoras que fueron
    desarrolladas en la Fórmula 1 terminaron siendo utilizadas en automóviles comerciales, como el freno de disco.<br>

    El inicio de la Fórmula 1 moderna se remonta al año 1950, en el que participaron escuderías como Ferrari, Alfa Romeo y
    Maserati. Algunas fueron reemplazadas por otras nuevas como McLaren, Williams, Red Bull y otras que volvieron como
    Mercedes que se han alzado varias veces con el Campeonato Mundial de Constructores. Por su parte, los pilotos deben
    contar con la superlicencia de la FIA para competir, que se obtiene por los resultados en otros campeonatos.
